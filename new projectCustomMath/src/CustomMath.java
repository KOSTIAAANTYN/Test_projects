public class CustomMath {
        static int plus(int a, int b){
            return a+b;
        }
        static double plus(double a, double b){
            return a+b;
        }
        static int minus(int a, int b){
            return a-b;
        }
        static double minus(double a,double b){
            return a-b;
        }
        static double pow(int a,int n){
            return Math.pow(a,n);
        }

    }
